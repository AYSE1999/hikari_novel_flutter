class CommentItem {
  final String rid;
  final String content;
  final String viewCount;
  final String replyCount;
  final String userName;
  final String uid;
  final String time;

  CommentItem({
    required this.rid,
    required this.content,
    required this.viewCount,
    required this.replyCount,
    required this.userName,
    required this.uid,
    required this.time,
  });
}
